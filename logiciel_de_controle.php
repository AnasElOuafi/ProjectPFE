                                <a class="h2 m-0 text-white" href="">Un interphone est un dispositif de communication bidirectionnel utilisé pour permettre la communication vocale ou vidéo entre deux points, généralement entre l'extérieur et l'intérieur d'un bâtiment. 
                                    L'installation d'un interphone implique la fixation d'une unité extérieure, généralement munie d'une caméra et d'un microphone, près de l'entrée, ainsi qu'une unité intérieure dotée d'un haut-parleur et d'un écran (dans le cas d'un interphone vidéo) à l'intérieur du bâtiment. Une fois installé, l'interphone permet aux visiteurs d'annoncer leur présence à l'entrée et aux occupants de l'intérieur de communiquer avec les visiteurs avant de leur permettre l'accès au bâtiment. 
